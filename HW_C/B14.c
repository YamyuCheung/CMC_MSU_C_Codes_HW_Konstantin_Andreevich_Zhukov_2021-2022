#include <stdio.h>

int main(int argc, const char * argv[]) {
    enum {size = 10};
    int up[size] = {0}, i, j, temp;
    for (i = 0; i < size; i++)
    {
        scanf("%d", up+i);
    }
    for (i = 0; i < size-1; i++)
    {
        for(j = 0; j < size-i-1; j++)
        {
            if(up[j]%10 > up[j+1]%10)
            {
                temp = up[j];
                up[j] = up[j+1];
                up[j+1] = temp;
            }
        }
    }
    for (i = 0; i < size; i++)
    {
        printf("%d ", up[i]);
    }
    printf("\n");
    return 0;
}
