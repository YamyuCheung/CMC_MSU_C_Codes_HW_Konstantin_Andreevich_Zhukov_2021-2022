#include <stdio.h>
#include <math.h>

int main(int argc, const char * argv[]) {
    float a, b, c, hsum, S;
    scanf("%f %f %f", &a, &b, &c);
    hsum = (a + b + c)/2;
    S = sqrt(hsum*(hsum-a)*(hsum-b)*(hsum-c));
    if ((a+b > c) & (b+c > a) & (a+c > b))
        printf("%.2f\n", S);
    else
        printf("-1\n");
    return 0;
}
