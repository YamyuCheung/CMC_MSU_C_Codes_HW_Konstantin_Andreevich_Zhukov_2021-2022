#include <stdio.h>

int main(int argc, const char * argv[]) {
    int sum = 0, ch;
    do{
        ch = getchar();
        if (ch >= '0' && ch <= '9')
            sum += ch - '0';
    }while(ch != '.');
    printf("%d\n", sum);
    return 0;
}
