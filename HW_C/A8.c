#include <stdio.h>

int main(int argc, const char * argv[]) {
    int num, min;
    scanf("%d", &num);
    min = num%10;
    while(num)
    {
        if (min > num%10)
            min = num%10;
        num /= 10;
        
    }
    printf("%d\n", min);
    return 0;
}
