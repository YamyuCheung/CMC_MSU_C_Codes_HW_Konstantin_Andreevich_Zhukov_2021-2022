#include <stdio.h>

int main(int argc, const char * argv[]) {
    int months[] = {31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
    int m, d, counter, sum=0;
    scanf("%d %d", &m, &d);
    if (m>2)
    {
        for (counter = 2; counter < m-1; counter++)
        {
            sum+=months[counter];
        }
        sum = sum + 16 + d;
    }
    else
        sum = d - 12;
    
    printf("%d\n", sum);
    return 0;
}
