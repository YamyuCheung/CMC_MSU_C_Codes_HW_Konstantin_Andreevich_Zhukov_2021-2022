#include <stdio.h>

int main(int argc, const char * argv[]) {
    int ch, j=0, i=0, flag = 0;

    do{
        ch = getchar();
        if (ch == ' ' || ch == '\n')
            break;
        if (ch >= '0' && ch <= '9')
            i=1;
        if (ch == '-')
        {
            if (flag == 0)
            {
                flag = 1;
                continue;
            }
            else
            {
                flag = 2;
                continue;
            }
        }
        if (ch < '0' || ch > '9')
            j=1;
    }while(ch != ' ' || ch != '\n');

    if (i==1 && !j && flag != 2)
        printf("YES\n");
    else
        printf("NO\n");
    return 0;
}
