#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int main(int argc, const char * argv[])
{
    unsigned short n, t;
    char ch;
    scanf("%hd %c %hd", &n, &ch, &t);
//    printf("%hd %c %hd\n", n, ch, t);

    if (ch == 'u')
    {
        t <<= 6;
        n = n | t;
    }
    else if (ch == 'g')
    {
        t <<= 3;
        n = n | t;
    }
    else if (ch == 'o')
    {
        n = n | t;
    }

    printf("%hd\n", n);


    return 0;
}
