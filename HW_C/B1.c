#include <stdio.h>

int power(int a, int b)
{
    int pow=1, i;
    for(i=0; i<b; i++)
        pow *= a;
    return pow;
}

int main(int argc, const char * argv[]) {
    int num1, num2;
    scanf("%d %d", &num1, &num2);
    printf("%d\n", power(num1, num2));
    return 0;
}
