#include <stdio.h>

int is_digit(char c)
{
    int d = 0;
    if (c >= '0' && c <= '9')
        d = 1;
    return d;
}

int main(int argc, const char * argv[]) {
    char c, k = 0, digit;
    do{
        scanf("%c", &c);
        digit = is_digit(c);
        if (digit == 1)
            k++;
    }while(c != '.');
    printf("%d\n", k);
    return 0;
}
