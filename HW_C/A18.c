#include <stdio.h>

int main(int argc, const char * argv[]) {
    int ch;
    do {
        ch = getchar();
        if (ch >= 'A' && ch <= 'Z')
            ch = ch + 'a' - 'A';
        if (ch != '.')
            printf("%c", ch);
        } while(ch != '.');
    printf("\n");
    return 0;
}
